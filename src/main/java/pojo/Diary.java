package pojo;

public class Diary {
	private Integer dia_id;
	private Integer admin_id;
	private String dia_type;
	private String dia_time;
	private String dia_object;
	private String dia_text;

	public Integer getDia_id() {
		return dia_id;
	}

	public void setDia_id(Integer dia_id) {
		this.dia_id = dia_id;
	}

	public Integer getAdmin_id() {
		return admin_id;
	}

	public void setAdmin_id(Integer admin_id) {
		this.admin_id = admin_id;
	}

	public String getDia_type() {
		return dia_type;
	}

	public void setDia_type(String dia_type) {
		this.dia_type = dia_type;
	}

	public String getDia_time() {
		return dia_time;
	}

	public void setDia_time(String dia_time) {
		this.dia_time = dia_time;
	}

	public String getDia_object() {
		return dia_object;
	}

	public void setDia_object(String dia_object) {
		this.dia_object = dia_object;
	}

	public String getDia_text() {
		return dia_text;
	}

	public void setDia_text(String dia_text) {
		this.dia_text = dia_text;
	}

}
