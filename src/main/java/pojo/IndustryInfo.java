package pojo;

public class IndustryInfo {
	private Integer ind_info_id;
	private Integer ind_id;
	private String ind_date;
	private String url;
	private String title;
	private String source;
	private String author;
	private String text;
	private String picture;

	public Integer getInd_info_id() {
		return ind_info_id;
	}

	public void setInd_info_id(Integer ind_info_id) {
		this.ind_info_id = ind_info_id;
	}

	public Integer getInd_id() {
		return ind_id;
	}

	public void setInd_id(Integer ind_id) {
		this.ind_id = ind_id;
	}

	public String getInd_date() {
		return ind_date;
	}

	public void setInd_date(String ind_date) {
		this.ind_date = ind_date;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSource() {
		return source;
	}

	public void setSource(String source) {
		this.source = source;
	}

	public String getAuthor() {
		return author;
	}

	public void setAuthor(String author) {
		this.author = author;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getPicture() {
		return picture;
	}

	public void setPicture(String picture) {
		this.picture = picture;
	}

}
