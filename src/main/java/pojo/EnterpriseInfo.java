package pojo;

public class EnterpriseInfo {
	private Integer ent_id;
	private Integer ind_id;
	private String ent_name;
	private String ent_num;
	private String ent_add;
	private String ent_phone;
	private String ent_text;

	public Integer getEnt_id() {
		return ent_id;
	}

	public void setEnt_id(Integer ent_id) {
		this.ent_id = ent_id;
	}

	public Integer getInd_id() {
		return ind_id;
	}

	public void setInd_id(Integer ind_id) {
		this.ind_id = ind_id;
	}

	public String getEnt_name() {
		return ent_name;
	}

	public void setEnt_name(String ent_name) {
		this.ent_name = ent_name;
	}

	public String getEnt_num() {
		return ent_num;
	}

	public void setEnt_num(String ent_num) {
		this.ent_num = ent_num;
	}

	public String getEnt_add() {
		return ent_add;
	}

	public void setEnt_add(String ent_add) {
		this.ent_add = ent_add;
	}

	public String getEnt_phone() {
		return ent_phone;
	}

	public void setEnt_phone(String ent_phone) {
		this.ent_phone = ent_phone;
	}

	public String getEnt_text() {
		return ent_text;
	}

	public void setEnt_text(String ent_text) {
		this.ent_text = ent_text;
	}
}
